function dobrarNumero(numero){
    /**
     * Retorna o dobro de um numero
     * @param {number} numero - O numero a ser dobrado
     * @returns {number} O dobro do numero
     */
    return numero * 2;
}

//Exemplos de uso:
console.log(dobrarNumero(6));
console.log(dobrarNumero(8));
console.log(dobrarNumero(5.5));

