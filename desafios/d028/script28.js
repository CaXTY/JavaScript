function calcularMedia(a, b, c) {
    /**
     * Calcula a média de três números
     * @param {number} a - Primeiro numero
     * @param {number} b - Segundo numero
     * @param {number} c - Terceiro numero
     * @returns {number} A media dos tres numeros
     */

    return (a + b + c) / 3;
}

//Exemplo de uso:
console.log(calcularMedia(15, 30, 60));
console.log(calcularMedia(5, 4, 3));
console.log(calcularMedia(9, 8, 7));
